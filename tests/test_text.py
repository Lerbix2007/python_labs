import pytest
from lib.text_lib import normalize, tokenize, count_freq, top_n


@pytest.mark.parametrize(
    "source, expected",
    [
        ("–ü—Ä–ò–≤–ï—Ç\\n–ú–ò—Ä\\t", "–ø—Ä–∏–≤–µ—Ç\\n–º–∏—Ä\\t"),
        ("—ë–∂–∏–∫, –Å–ª–∫–∞", "–µ–∂–∏–∫, –µ–ª–∫–∞"),
        ("Hello\\r\\nWorld", "hello\\r\\nworld"),
        ("  –¥–≤–æ–π–Ω—ã–µ   –ø—Ä–æ–±–µ–ª—ã  ", "–¥–≤–æ–π–Ω—ã–µ –ø—Ä–æ–±–µ–ª—ã"),
    ],
)
def test_normalize_basic(source, expected):
    assert normalize(source) == expected


@pytest.mark.parametrize(
    "source, expected",
    [
        ("–ø—Ä–∏–≤–µ—Ç –º–∏—Ä", ["–ø—Ä–∏–≤–µ—Ç", "–º–∏—Ä"]),
        ("", []),
        ("hello,world!!!", ["hello", "world"]),
        ("–ø–æ-–Ω–∞—Å—Ç–æ—è—â–µ–º—É –∫—Ä—É—Ç–æ", ["–ø–æ-–Ω–∞—Å—Ç–æ—è—â–µ–º—É", "–∫—Ä—É—Ç–æ"]),
        ("2025 –≥–æ–¥", ["2025", "–≥–æ–¥"]),
        ("emoji üòÄ –Ω–µ —Å–ª–æ–≤–æ", ["emoji", "–Ω–µ", "—Å–ª–æ–≤–æ"]),
    ],
)
def test_tokenize_basic(source, expected):
    assert tokenize(source) == expected


@pytest.mark.parametrize(
    "case_t, expected",
    [
        (([], {}, 2), ({}, [])),
        (
            (["a", "b", "a", "c", "b", "a"], {"a": 3, "b": 2, "c": 1}, 2),
            ({"a": 3, "b": 2, "c": 1}, [("a", 3), ("b", 2)]),
        ),
        (
            (
                ["—Ç–µ—Å—Ç—ã", "–¥–ª—è", "–ª–∞–±–æ—Ä–∞—Ç–æ—Ä–Ω–æ–π", "—Ç–µ—Å—Ç—ã", "–Ω–æ–º–µ—Ä"],
                {"—Ç–µ—Å—Ç—ã": 2, "–¥–ª—è": 1, "–ª–∞–±–æ—Ä–∞—Ç–æ—Ä–Ω–æ–π": 1, "–Ω–æ–º–µ—Ä": 1},
                3,
            ),
            (
                {"—Ç–µ—Å—Ç—ã": 2, "–¥–ª—è": 1, "–ª–∞–±–æ—Ä–∞—Ç–æ—Ä–Ω–æ–π": 1, "–Ω–æ–º–µ—Ä": 1},
                [("—Ç–µ—Å—Ç—ã", 2), ("–¥–ª—è", 1), ("–ª–∞–±–æ—Ä–∞—Ç–æ—Ä–Ω–æ–π", 1)],
            ),
        ),
        ((["—Å–µ–º—å", "—Å–µ–º—å", "—Å–µ–º—å"], {"—Å–µ–º—å": 3}, 1), ({"—Å–µ–º—å": 3}, [("—Å–µ–º—å", 3)])),
        (
            (
                ["–ª–∞–±–æ—Ä–∞—Ç–æ—Ä–Ω–æ–π", "–Ω–æ–º–µ—Ä", "–ª–∞–±–æ—Ä–∞—Ç–æ—Ä–Ω–æ–π", "—Ç–µ—Å—Ç—ã", "–Ω–æ–º–µ—Ä"],
                {"–ª–∞–±–æ—Ä–∞—Ç–æ—Ä–Ω–æ–π": 2, "–Ω–æ–º–µ—Ä": 2, "—Ç–µ—Å—Ç—ã": 1},
                3,
            ),
            (
                {"–ª–∞–±–æ—Ä–∞—Ç–æ—Ä–Ω–æ–π": 2, "–Ω–æ–º–µ—Ä": 2, "—Ç–µ—Å—Ç—ã": 1},
                [("–ª–∞–±–æ—Ä–∞—Ç–æ—Ä–Ω–æ–π", 2), ("–Ω–æ–º–µ—Ä", 2), ("—Ç–µ—Å—Ç—ã", 1)],
            ),
        ),
        (
            (["bb", "aa", "bb", "aa", "cc"], {"aa": 2, "bb": 2, "cc": 1}, 2),
            ({"aa": 2, "bb": 2, "cc": 1}, [("aa", 2), ("bb", 2)]),
        ),
    ],
)
def test_count_freq_and_top_n(case_t, expected):
    assert count_freq(case_t[0]) == expected[0]
    assert top_n(case_t[1], case_t[2]) == expected[1]
